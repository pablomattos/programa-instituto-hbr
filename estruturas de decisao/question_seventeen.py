# Faça um algoritmo para ler dois números inteiros
# e informar se estes números são iguais ou se são diferentes

print("Program that checks if the numbers entered are the same or not")
first_number = int(input("Enter a number: "))
second_number = int(input("Enter other number: "))

if first_number == second_number:
    print(f"The numbers {first_number} and {second_number} are the same number.")

else:
    print(f"The numbers {first_number} and {second_number} are not the same number.")